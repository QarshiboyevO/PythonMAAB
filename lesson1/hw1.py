# 2. If analyzing and sorting large data manually in Excel is difficult, how do you think Python can help solve this problem?
#
# Python can handle large datasets much faster and more efficiently than manual Excel work. Using libraries like Pandas, Python can automatically clean, sort, filter, and analyze thousands or millions of rows of data with a few lines of code. This reduces human errors, saves time, and allows repeated analysis without starting from scratch.
